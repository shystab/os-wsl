int
sys_settickets(void)
{
  int n;
  if(argint(0, &n) < 0) return -1;  // 取参数
  if(n < 1) return -1;               // 票数至少 1
  myproc()->tickets = n;
  return 0;
}
int sys_getpinfo(void) {
  struct pstat *ps;
  if(argptr(0, (void*)&ps, sizeof(*ps)) < 0) return -1;  // 取指针参数
  fill_pstat(ps);  // 遍历进程表填数据（需要在 proc.c 实现）
  return 0;
}
